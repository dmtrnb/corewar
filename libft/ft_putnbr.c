#include "libft.h"

void	ft_putnbr(int n)
{
	int		i;
	int		znak;
	char	buf[12];

	i = 11;
	while (--i >= 0)
		*(buf + i) = ' ';
	*(buf + 11) = 0;
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		i = (n == 0 ? 9 : 10);
		znak = (n < 0 ? 1 : 0);
		*(buf + 10) = (n == 0 ? '0' : ' ');
		n *= (znak ? -1 : 1);
		while (n)
		{
			*(buf + i) = n % 10 + 48;
			n /= 10;
			i--;
		}
		*(buf + i) = (znak ? '-' : ' ');
		ft_putstr(buf + i + 1 - znak);
	}
}
