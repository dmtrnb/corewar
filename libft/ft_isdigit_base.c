#include "libft.h"

int		ft_isdigit_base(int c, int base)
{
	if (base > 0 && base <= 10)
	{
		if (c > 47 && c < 48 + base)
			return (1);
	}
	else if (base > 10 && base < 37)
		if ((c > 47 && c < 58) || (c > 64 && c < 55 + base))
			return (1);
	return (0);
}
